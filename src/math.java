public class math {
	public static void main(String args[]) {
		//Absolute value. How far from 0
		System.out.println(Math.abs(-26));
		
		//Rounds it off to the upper
		System.out.println(Math.ceil(7.6));
		
		//Rounds it off to the lower
		System.out.println(Math.floor(7.6));
		
		//Returns the maximum
		System.out.println(Math.max(7.6, 56.0));
		
		//Returns the minimum
		System.out.println(Math.min(7.6, 7));
		
		//Returns the number raided to a particular power
		System.out.println(Math.pow(5, 3));
		
		//Square root
		System.out.println(Math.sqrt(9));
	}

}
